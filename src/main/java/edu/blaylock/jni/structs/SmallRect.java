package edu.blaylock.jni.structs;

public class SmallRect {
    public short left;
    public short top;
    public short right;
    public short bottom;

    public SmallRect(short left, short top, short right, short bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
